fun main() {
    val Point = 60

    if (Point >= 75){
        println("Selamat nilai anda sudah diatas batas rata-rata")
    }
    else if (Point >= 60){
        println("Nilai anda dibawah batas rata, namun diatas batas lulus. Tolong tingkatkan lagi")
    }
    else{
        println("Maaf nilai anda kurang dari 60, anda dinyatakan tidak lulus")
    }
}